# Task 2: String Methods and Escape Characters
# Create a string sentence = "Python is Fun".
# Print the following:
sentence = "Python is Fun"

# Does the string end with "Fun"?
print(sentence.endswith("Fun"))

# Does the string start with "Python"?
print(sentence.startswith("Python"))

# The capitalized form of the string
print(sentence.capitalize())

# The length of the string
print(len(sentence))

